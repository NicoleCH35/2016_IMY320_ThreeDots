function validateLogin()
{
   
	var done2 = true;
	$(".Linput").each(function()
	{
		if($(this).val() == '')
		{
			$(this).css('border-color', 'red');
			done2 = false;
		}

	});

	if(done2)
	{
		return true;
	}
	else
	{
		return false;
	}

}
